package br.gov.pi.ati.agendacultural.modelo.eventos;

import java.io.Serializable;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

/**
 *
 * @author Juniel
 */
@Entity
public class HistoricoPortfolio implements Serializable {

    @Id
    @SequenceGenerator(name = "HistoricoPortfolio", allocationSize = 1, sequenceName = "historicoPortfolio_id_seq")
    @GeneratedValue(generator = "HistoricoPortfolio")
    private Long id;
    
    @Column(columnDefinition = "Text")
    private String historico;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getHistorico() {
        return historico;
    }

    public void setHistorico(String historico) {
        this.historico = historico;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 79 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final HistoricoPortfolio other = (HistoricoPortfolio) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }
    
    
}
