package br.gov.pi.ati.passelivre.modelo.cadastro;

import br.gov.pi.ati.passelivre.modelo.enums.StatusResponsavel;
import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import org.hibernate.validator.constraints.NotBlank;

/**
 *
 * @author Juniel
 */
@Entity
public class Responsavel implements Serializable {

    @Id
    @SequenceGenerator(name = "Responsavel", sequenceName = "responsavel_id_seq")
    @GeneratedValue(generator = "Responsavel")
    private Long id;

    @Size(max = 50)
    @NotBlank
    private String nome;

    @Size(max = 50)
    @NotBlank
    private String funcao;

    @NotNull
    private StatusResponsavel status;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getFuncao() {
        return funcao;
    }

    public void setFuncao(String funcao) {
        this.funcao = funcao;
    }

    public StatusResponsavel getStatus() {
        return status;
    }

    public void setStatus(StatusResponsavel status) {
        this.status = status;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 17 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Responsavel other = (Responsavel) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return nome;
    }

}
