package br.gov.pi.ati.sccd.modelo.cadastro;

import br.gov.pi.ati.sccd.modelo.certificado.Certificado;
import com.xpert.audit.NotAudited;
import java.io.Serializable;
import java.util.List;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.ManyToOne;
import javax.persistence.OneToMany;
import javax.persistence.SequenceGenerator;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import org.hibernate.validator.constraints.NotBlank;

/**
 *
 * @author Juniel
 */
@Entity
public class AutoridadeCertificadora implements Serializable {

    @Id
    @SequenceGenerator(name = "AutoridadeCertificadora", sequenceName = "autoridadeCertificadora_id_seq")
    @GeneratedValue(generator = "AutoridadeCertificadora")
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @NotNull
    private Fornecedor fornecedor;
    
    @Size(max = 250)
    @NotBlank
    private String nome;
    
    private boolean ativo = true;
    
    @NotAudited
    @OneToMany(mappedBy = "titular")
    private List<Certificado> certificados;

    @Override
    public String toString() {
        return nome; 
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Fornecedor getFornecedor() {
        return fornecedor;
    }

    public void setFornecedor(Fornecedor fornecedor) {
        this.fornecedor = fornecedor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public boolean isAtivo() {
        return ativo;
    }

    public void setAtivo(boolean ativo) {
        this.ativo = ativo;
    }

    public List<Certificado> getCertificados() {
        return certificados;
    }

    public void setCertificados(List<Certificado> certificados) {
        this.certificados = certificados;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 47 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final AutoridadeCertificadora other = (AutoridadeCertificadora) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }
    
    
}
