package br.gov.pi.ati.modelo.orcamento;

import java.io.Serializable;
import java.math.BigDecimal;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import org.hibernate.validator.constraints.NotBlank;

/**
 *
 * @author Juniel
 */
@Entity
public class ReceitaMetaAcaoEstrategica implements Serializable {

    @Id
    @SequenceGenerator(name = "ReceitaMetaAcaoEstrategica", sequenceName = "receitaMetaAcaoEstrategica_id_seq")
    @GeneratedValue(generator = "ReceitaMetaAcaoEstrategica")
    private Long id;

    @Size(max = 250)
    @NotBlank
    private String nome;

    @NotNull
    private BigDecimal valor;

    @Override
    public String toString() {
        return nome;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public BigDecimal getValor() {
        return valor;
    }

    public void setValor(BigDecimal valor) {
        this.valor = valor;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        hash = 89 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final ReceitaMetaAcaoEstrategica other = (ReceitaMetaAcaoEstrategica) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

}
