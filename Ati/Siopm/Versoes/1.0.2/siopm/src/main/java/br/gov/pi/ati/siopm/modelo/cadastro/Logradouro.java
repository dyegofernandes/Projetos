package br.gov.pi.ati.siopm.modelo.cadastro;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import javax.persistence.CascadeType;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.ManyToOne;
import javax.persistence.SequenceGenerator;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import org.hibernate.validator.constraints.NotBlank;

/**
 *
 * @author Juniel
 */
//@Entity
public class Logradouro implements Serializable {

    @Id
    @SequenceGenerator(name = "Logradouro", sequenceName = "logradouro_id_seq")
    @GeneratedValue(generator = "Logradouro")
    private Long id;

    @Size(max = 260)
    @NotBlank
    private String logradouro;

    @NotNull
    @ManyToOne(fetch = FetchType.LAZY)
    private Cidade cidade;

    @ManyToMany(targetEntity = Bairro.class, fetch = FetchType.LAZY, cascade = {CascadeType.MERGE})
    private List<Bairro> bairros = new ArrayList<Bairro>();

    @Override
    public String toString() {
        return logradouro;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getLogradouro() {
        return logradouro;
    }

    public void setLogradouro(String logradouro) {
        if (logradouro != null) {
            logradouro = logradouro.trim().toUpperCase();
        }
        this.logradouro = logradouro;
    }

    public Cidade getCidade() {
        return cidade;
    }

    public void setCidade(Cidade cidade) {
        this.cidade = cidade;
    }

    public List<Bairro> getBairros() {
        return bairros;
    }

    public void setBairros(List<Bairro> bairros) {
        this.bairros = bairros;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 97 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Logradouro other = (Logradouro) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

}
