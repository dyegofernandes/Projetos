package br.gov.pi.ati.siopm.modelo.cadastro;

import java.io.Serializable;
import java.util.Date;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;
import javax.validation.constraints.NotNull;

/**
 *
 * @author Juniel
 */
@Entity
public class PatrulhaLocalizacao implements Serializable {

    @Id
    @SequenceGenerator(name = "PatrulhaLocalizacao", sequenceName = "patrulhaLocalizacao_id_seq")
    @GeneratedValue(generator = "PatrulhaLocalizacao")
    private Long id;

    @Temporal(TemporalType.TIMESTAMP)
    @NotNull
    private Date diaEHora = new Date();

    private double latitude;

    private double longitude;

    public Date getDiaEHora() {
        return diaEHora;
    }

    public void setDiaEHora(Date diaEHora) {
        this.diaEHora = diaEHora;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 17 * hash + (this.id != null ? this.id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final PatrulhaLocalizacao other = (PatrulhaLocalizacao) obj;
        if (this.id != other.id && (this.id == null || !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

}
