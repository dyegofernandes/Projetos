package br.gov.pi.ati.sisdh.modelo.cadastro.enums;

public enum Situacao {

    NOVA(1, "Novo"),
    REFERENCIADA(2, "Referenciada"),
    ACEITA(3, "Aceita");

    private Situacao(int num, String descricao) {
        this.descricao = descricao;
        this.num = num;
    }
    private String descricao;
    private int num;

    public String getDescricao() {
        return descricao;
    }

    public int getNum() {
        return num;
    }
    
}

