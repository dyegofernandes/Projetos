package br.com.jsoft.centralfinanceira.modelo.arquivo;

/**
 *
 * @author KillerBeeTwo
 */
public enum TipoRepasse {
    CLARO("Claro"), 
    RELATORIOSITE("Relatorio Site"),
    REPASSEFLUXO("Repasse Fluxo"),
    REPASSESITE("Repasse Site");
    
    private final String descricao;

    private TipoRepasse(String descricao) {
        this.descricao = descricao;
    }

    public String getDescricao() {
        return descricao;
    }
}
