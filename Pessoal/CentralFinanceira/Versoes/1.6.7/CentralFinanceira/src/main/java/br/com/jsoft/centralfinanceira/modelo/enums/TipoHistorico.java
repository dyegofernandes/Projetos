public enum TipoHistorico {

    LANCAMENTO("Lançamentos"),
    TRANSFERENCIA("Transferencias"),
    CONTAS_A_RECEBER("Contas a Receber"),
    CONTAS_A_PAGAR("Contas a Pagar"),
    NOTAS_FISCAIS_SAIDA("Notas Fiscais de Saída"),
    ADIANTAMENTO_A_FORNECEDORES("Adiantamento a Fornecedores"),
    ADIANTAMENTO_A_CLIENTES("Adiantamento a Clientes"),
    PEDIDO_DE_VENDA("Pedido de Venda");

    private TipoHistorico(String descricao) {
        this.descricao = descricao;
    }

    String descricao;

    public String getDescricao() {
        return descricao;
    }

}
