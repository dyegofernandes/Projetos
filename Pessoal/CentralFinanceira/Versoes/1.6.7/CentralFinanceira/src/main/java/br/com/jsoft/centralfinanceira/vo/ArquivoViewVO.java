package br.com.jsoft.centralfinanceira.vo;

/**
 *
 * @author Juniel
 */
public class ArquivoViewVO {

    private String empresa;

    private String nome;

    private String diretorioPai;

    private String direotrioFilho;

    private String extensao;

    private String dataPublicacao;

    private String valor;

    private String path;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getExtensao() {
        return extensao;
    }

    public void setExtensao(String extensao) {
        this.extensao = extensao;
    }

    public String getEmpresa() {
        return empresa;
    }

    public void setEmpresa(String empresa) {
        this.empresa = empresa;
    }

    public String getDataPublicacao() {
        return dataPublicacao;
    }

    public void setDataPublicacao(String dataPublicacao) {
        this.dataPublicacao = dataPublicacao;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getDiretorioPai() {
        return diretorioPai;
    }

    public void setDiretorioPai(String diretorioPai) {
        this.diretorioPai = diretorioPai;
    }

    public String getDireotrioFilho() {
        return direotrioFilho;
    }

    public void setDireotrioFilho(String direotrioFilho) {
        this.direotrioFilho = direotrioFilho;
    }

}
