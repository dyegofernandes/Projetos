package br.com.politicalpartysystem.modelo.enums;

/**
 *
 * @author Juniel
 */
public enum Sex {

    MEN(1, "Masculino", "M"),
    FEMALE(2, "Feminino", "F");

    private Sex(int code, String text, String acronym) {
        this.text = text;
        this.code = code;
        this.acronym = acronym;
    }

    private String text;
    private int code;
    private String acronym;

    public String getText() {
        return text;
    }

    public int getCode() {
        return code;
    }

    public String getAcronym() {
        return acronym;
    }

}
