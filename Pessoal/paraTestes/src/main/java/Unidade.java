public class Unidade {

    private String nome;

    private Tipo tipo;

    public Unidade(String nome, Tipo tipo) {
        this.nome = nome;

        this.tipo = tipo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Tipo getTipo() {
        return tipo;
    }

    public void setTipo(Tipo tipo) {
        this.tipo = tipo;
    }

}
